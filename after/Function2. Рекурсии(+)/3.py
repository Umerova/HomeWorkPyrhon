def sum_numbers():
    """
    Функция запрашивает у пользователя числа и складывает их.
    Сигналом к окончанию ввода является пустая строка.
    Возвращает сумму введенных чисел.
    """
    number_str = input("Введите число (для окончания ввода оставьте строку пустой): ")
    if not number_str:  # если строка пустая
        return 0.0  # возвращаем 0.0
    number = float(number_str)
    return number + sum_numbers()  # рекурсивный вызов функции с последующим сложением

# вызов функции для подсчета суммы
total_sum = sum_numbers()

# вывод результата
print(f"Сумма введенных чисел: {total_sum}")
