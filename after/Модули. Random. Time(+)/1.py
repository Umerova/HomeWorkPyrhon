"""
В быстрых шахматах на принятие решений для всех ходов игроку даётся 30 минут. Программа должна:
Предлагать ввод хода (например, E2–E4) и считать потраченное время.
После получения хода печатать оставшееся время в минутах.
Если 30 минут закончились или игрок вводит «off» — завершать работу.
Оформить в виде функции.
"""
import time

def chess_timer():
    time_left = 30*60  # в секундах
    print(f'У вас есть {time_left//60} минут и {time_left%60} секунд на ход.')
    while True:
        move = input('Введите ход (например, E2–E4) или введите "off" для выхода: ')
        if move.lower() == 'off':
            print('Игра завершена.')
            break
        start_time = time.time()
        time_left -= int(time.time() - start_time)  # вычитаем время, потраченное на ввод хода
        if time_left <= 0:
            print('Время истекло.')
            break
        print(f'Осталось {time_left//60} минут и {time_left%60} секунд на ход.')
